    factor_conclusion TEXT,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    deleted TINYINT NOT NULL DEFAULT 0,
    INDEX idx_signal_id (signal_id),
    INDEX idx_factor_code (factor_code)
);

CREATE TABLE IF NOT EXISTS report_evidence_ref (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    evidence_id VARCHAR(64) NOT NULL UNIQUE,
    report_id VARCHAR(64) NOT NULL,
    task_id VARCHAR(64) DEFAULT NULL,
    section_code VARCHAR(64) DEFAULT NULL,
    conclusion_code VARCHAR(64) DEFAULT NULL,
    source_type VARCHAR(64) NOT NULL,
    source_ref_id VARCHAR(128) DEFAULT NULL,
    evidence_summary TEXT,
    evidence_url VARCHAR(512) DEFAULT NULL,
    confidence_score DECIMAL(5,4) DEFAULT NULL,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    deleted TINYINT NOT NULL DEFAULT 0,
    INDEX idx_report_id (report_id),
    INDEX idx_task_id (task_id),
    INDEX idx_source_ref (source_type, source_ref_id)
);

