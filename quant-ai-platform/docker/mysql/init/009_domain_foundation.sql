CREATE TABLE IF NOT EXISTS human_review_record (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    review_id VARCHAR(64) NOT NULL UNIQUE,
    task_id VARCHAR(64) DEFAULT NULL,
    related_object_type VARCHAR(64) NOT NULL,
    related_object_id VARCHAR(64) NOT NULL,
    reviewer_id VARCHAR(64) DEFAULT NULL,
    reviewer_role VARCHAR(64) DEFAULT NULL,
    review_result VARCHAR(32) NOT NULL,
    review_comment VARCHAR(1000) DEFAULT NULL,
    before_snapshot_ref VARCHAR(512) DEFAULT NULL,
    after_snapshot_ref VARCHAR(512) DEFAULT NULL,
    before_snapshot JSON DEFAULT NULL,
    after_snapshot JSON DEFAULT NULL,
    trace_id VARCHAR(128) DEFAULT NULL,
    tenant_id VARCHAR(64) NOT NULL DEFAULT 'default',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    deleted TINYINT NOT NULL DEFAULT 0,
    INDEX idx_task_id (task_id),
    INDEX idx_related_object (related_object_type, related_object_id),
    INDEX idx_reviewer_id (reviewer_id),
    INDEX idx_review_result (review_result)
);

CREATE TABLE IF NOT EXISTS task_message_log (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    message_log_id VARCHAR(64) NOT NULL UNIQUE,
    message_id VARCHAR(64) NOT NULL,
    task_id VARCHAR(64) DEFAULT NULL,
    event_id VARCHAR(64) DEFAULT NULL,
    topic_name VARCHAR(128) NOT NULL,
    message_type VARCHAR(64) NOT NULL,
    producer_service VARCHAR(128) DEFAULT NULL,
    consumer_service VARCHAR(128) DEFAULT NULL,
    consume_status VARCHAR(32) NOT NULL,
    retry_count INT NOT NULL DEFAULT 0,
    error_message VARCHAR(1000) DEFAULT NULL,
    raw_message_ref VARCHAR(512) DEFAULT NULL,
    message_timestamp BIGINT DEFAULT NULL,
    trace_id VARCHAR(128) DEFAULT NULL,
    tenant_id VARCHAR(64) NOT NULL DEFAULT 'default',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    deleted TINYINT NOT NULL DEFAULT 0,
    INDEX idx_message_id (message_id),
    INDEX idx_task_id (task_id),
    INDEX idx_event_id (event_id),
    INDEX idx_topic_status (topic_name, consume_status),
    INDEX idx_trace_id (trace_id),
    INDEX idx_created_at (created_at)
);
