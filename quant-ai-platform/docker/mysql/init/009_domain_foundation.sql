USE quant_ai;

CREATE TABLE IF NOT EXISTS market_event_relation (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    event_id VARCHAR(64) NOT NULL,
    relation_type VARCHAR(32) NOT NULL,
    relation_code VARCHAR(64) NOT NULL,
    relation_name VARCHAR(255) DEFAULT NULL,
    relation_weight DECIMAL(8,4) DEFAULT NULL,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    deleted TINYINT NOT NULL DEFAULT 0,
    UNIQUE KEY uk_event_relation (event_id, relation_type, relation_code),
    INDEX idx_event_id (event_id),
    INDEX idx_relation_code (relation_code)
);

CREATE TABLE IF NOT EXISTS market_event_analysis (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    analysis_id VARCHAR(64) NOT NULL UNIQUE,
    event_id VARCHAR(64) NOT NULL,
    task_id VARCHAR(64) DEFAULT NULL,
    analysis_version VARCHAR(32) NOT NULL DEFAULT '1.0',
    impact_direction VARCHAR(32) DEFAULT NULL,
    impact_level VARCHAR(32) DEFAULT NULL,
    short_term_view TEXT,
    mid_term_view TEXT,
    risk_flag TINYINT NOT NULL DEFAULT 0,
    confidence_score DECIMAL(5,4) DEFAULT NULL,
    analysis_summary TEXT,
    status VARCHAR(32) NOT NULL DEFAULT 'ACTIVE',
    trace_id VARCHAR(128) DEFAULT NULL,
    tenant_id VARCHAR(64) NOT NULL DEFAULT 'default',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    deleted TINYINT NOT NULL DEFAULT 0,
    INDEX idx_event_id (event_id),
    INDEX idx_task_id (task_id),
    INDEX idx_status (status),
    INDEX idx_impact_level (impact_level)
);

CREATE TABLE IF NOT EXISTS risk_warning (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    warning_id VARCHAR(64) NOT NULL UNIQUE,
    task_id VARCHAR(64) DEFAULT NULL,
    warning_type VARCHAR(64) NOT NULL,
    warning_level VARCHAR(32) NOT NULL,
    entity_type VARCHAR(32) NOT NULL DEFAULT 'STOCK',
    entity_code VARCHAR(64) NOT NULL,
    entity_name VARCHAR(255) DEFAULT NULL,
    portfolio_id VARCHAR(64) DEFAULT NULL,
    trigger_source VARCHAR(64) DEFAULT NULL,
    trigger_event_id VARCHAR(64) DEFAULT NULL,
    warning_summary TEXT,
    warning_reason TEXT,
    suggest_action TEXT,
    confidence_score DECIMAL(5,4) DEFAULT NULL,
    status VARCHAR(32) NOT NULL DEFAULT 'ACTIVE',
    review_status VARCHAR(32) NOT NULL DEFAULT 'PENDING',
    reviewer_id VARCHAR(64) DEFAULT NULL,
    review_time DATETIME DEFAULT NULL,
    trace_id VARCHAR(128) DEFAULT NULL,
    tenant_id VARCHAR(64) NOT NULL DEFAULT 'default',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    deleted TINYINT NOT NULL DEFAULT 0,
    INDEX idx_task_id (task_id),
    INDEX idx_entity_level (entity_code, warning_level),
    INDEX idx_status_review (status, review_status),
    INDEX idx_trigger_event_id (trigger_event_id),
    INDEX idx_created_at (created_at)
);

CREATE TABLE IF NOT EXISTS risk_warning_detail (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    detail_id VARCHAR(64) NOT NULL UNIQUE,
    warning_id VARCHAR(64) NOT NULL,
    indicator_code VARCHAR(64) DEFAULT NULL,
    indicator_name VARCHAR(128) DEFAULT NULL,
    indicator_value VARCHAR(128) DEFAULT NULL,
    threshold_value VARCHAR(128) DEFAULT NULL,
    comparison_result VARCHAR(64) DEFAULT NULL,
    detail_desc TEXT,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    deleted TINYINT NOT NULL DEFAULT 0,
    INDEX idx_warning_id (warning_id),
    INDEX idx_indicator_code (indicator_code)
);

CREATE TABLE IF NOT EXISTS strategy_signal (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    signal_id VARCHAR(64) NOT NULL UNIQUE,
    task_id VARCHAR(64) DEFAULT NULL,
    signal_type VARCHAR(64) NOT NULL,
    entity_code VARCHAR(64) NOT NULL,
    entity_name VARCHAR(255) DEFAULT NULL,
    signal_date DATE DEFAULT NULL,
    signal_score INT DEFAULT NULL,
    signal_level VARCHAR(32) DEFAULT NULL,
    signal_direction VARCHAR(32) DEFAULT NULL,
    reason_summary TEXT,
    confidence_score DECIMAL(5,4) DEFAULT NULL,
    source_event_id VARCHAR(64) DEFAULT NULL,
    status VARCHAR(32) NOT NULL DEFAULT 'ACTIVE',
    trace_id VARCHAR(128) DEFAULT NULL,
    tenant_id VARCHAR(64) NOT NULL DEFAULT 'default',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    deleted TINYINT NOT NULL DEFAULT 0,
    INDEX idx_task_id (task_id),
    INDEX idx_entity_signal_date (entity_code, signal_date),
    INDEX idx_direction_level (signal_direction, signal_level),
    INDEX idx_source_event_id (source_event_id),
    INDEX idx_created_at (created_at)
);

CREATE TABLE IF NOT EXISTS strategy_signal_factor (
